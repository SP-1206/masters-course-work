(array a 10)
(array b 10)
(define setup (size)
   (let ((i 0))
      (while (< i size)
        (seq (set a[i] (read)) (set i (+ i 1)))
      )
   )
)

(define outputarray_a(size)
   (let ((i 0))
      (while (< i size)
        (seq (write a[i]) (set i (+ i 1)))
      )
   )
)
(define outputarray_b(size)
   (let ((i 0))
      (while (< i size)
        (seq (write b[i]) (set i (+ i 1)))
      )
   )
)

(define modify (size)
   (let ((i 0))
      (while (< i size)
      (seq (set b[i] (+ a[i] 1)) (set i (+ i 1)))
      )
   )
)


(define main()
   (seq (setup 10)
        (outputarray_a 10) (write 9999999) (modify 10) (outputarray_b 10)
   )
)

